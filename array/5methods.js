// Push
// Pop
// Unshift
// shift

let cars=["bmw","swift","maruti","xuv"];
console.log(cars);
cars.push("toyota"); //add to last 
console.log(cars);
cars.pop();    //delete from last & return it  
console.log(cars);
cars.unshift("toyota","ferrari");  //add to start
console.log(cars);
cars.shift();  //delete from start & return it
console.log(cars);