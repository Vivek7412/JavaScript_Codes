// slice : copies a portion of an array

let colors=["red","yello","blue","orange","pink","white"];

console.log(colors.slice());
console.log(colors.slice(6)); //gives empty array
console.log(colors.slice(3));
console.log(colors.slice(3,4));
console.log(colors.slice(-2)); //last  elements
console.log(colors.slice(-3));
console.log(colors.slice(colors.length-2));
console.log(colors.slice(colors.length)); //gives empty array
console.log(colors.length-2);
